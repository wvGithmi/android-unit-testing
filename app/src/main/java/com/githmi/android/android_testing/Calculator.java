package com.githmi.android.android_testing;

public class Calculator {
    // Addition
    public double fun_add(double num1, double num2) {
        double ans = num1 + num2;
        return ans;
    }

    public double fun_sub(double num1, double num2) {
        double ans = num1 - num2;
        return ans;
    }

    public double fun_mul(double num1, double num2) {
        double ans = num1 * num2;
        return ans;
    }

    public double fun_div(double num1, double num2) {
        double ans = num1 / num2;
        return ans;
    }
}